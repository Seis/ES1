    private void atualizaTabela(){
        medicos = DAO.MedicoDAO.select();

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.setRowCount(0);

        for(Medico m : medicos){
            model.addRow(new Object[]{m.getNome(),m.getEspecialidade()});
        }
    }
    
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Medico m = new Medico(jTextField1.getText(), jTextField2.getText(), 0);
  
        DAO.MedicoDAO.insert(m);
        
        atualizaTabela();
        jTextField1.requestFocus();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int index = jTable1.getSelectedRow();
        
        if (index != -1){
            Medico m = new Medico(jTextField1.getText(), jTextField2.getText(), medicos.get(index).getCodigo());
            DAO.MedicoDAO.update(m);
            
            atualizaTabela();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione a Consulta a ser atualizada");
        }
        jTable1.setRowSelectionInterval(index, index);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int i = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        jTextField1.setText(model.getValueAt(i, 0).toString());
        jTextField2.setText(model.getValueAt(i, 1).toString());
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int[] index = jTable1.getSelectedRows();
        
        if (index.length != 0){
            for (int i : index) {
                Medico m;
                m = new Medico(jTextField1.getText(), jTextField2.getText(), medicos.get(i).getCodigo());
                try {
                    DAO.MedicoDAO.delete(m);
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(rootPane, "Esse mÃ©dico possui consultas, portanto nÃ£o pode ser excluÃ­do!");
                }
            }
            atualizaTabela();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Selecione o MÃ©dico a ser atualizado");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

